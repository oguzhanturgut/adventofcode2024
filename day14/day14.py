def part1():
    with open('input.txt') as f:
        lines = f.read().splitlines()
        robots = []
        for i, line in enumerate(lines):
            pos, vel = line.split(' ')
            pos = pos.split('=')[1]
            pos_x, pos_y = pos.split(',')
            vel = vel.split('=')[1]
            vel_x, vel_y = vel.split(',')
            # print(f'{pos_x=}, {pos_y=}, {vel_x=}, {vel_y=}')
            robots.append((int(pos_x), int(pos_y), int(vel_x), int(vel_y)))
        seconds = 100
        width = 101
        height = 103
        print(robots)
        final = []
        for robot in robots:
            x, y, vx, vy = robot
            final.append(((x + vx * seconds) % width, (y + vy * seconds) % height))

        vertical_center = (height - 1) // 2
        horizontal_center = (width - 1) // 2

        quadrants = [0, 0, 0, 0]

        for  pos_x, pos_y in final:
            if pos_x == horizontal_center or pos_y == vertical_center:
                continue
            if pos_x < horizontal_center:
                if pos_y < vertical_center:
                    quadrants[0] += 1
                else:
                    quadrants[1] += 1
            else:
                if pos_y < vertical_center:
                    quadrants[2] += 1
                else:
                    quadrants[3] += 1

        print(quadrants)
        top_left, top_right, bottom_left, bottom_right = quadrants

        print(top_left * top_right * bottom_left * bottom_right)


