def part2():
    with open('input.txt') as f:
        lines = f.read().splitlines()
        robots = []
        for i, line in enumerate(lines):
            pos, vel = line.split(' ')
            pos = pos.split('=')[1]
            pos_x, pos_y = pos.split(',')
            vel = vel.split('=')[1]
            vel_x, vel_y = vel.split(',')
            robots.append((int(pos_x), int(pos_y), int(vel_x), int(vel_y)))
        width = 101
        height = 103
        robot_count = len(robots)

        for sec in range(1, 100000):
            robot_positions = set()
            for robot in robots:
                x, y, vx, vy = robot
                robot_positions.add(((x + vx * sec) % width, (y + vy * sec) % height))
            # Since the explanation of the problem is not clear,
            # I am assuming that the robots will form a picture of a Christmas tree
            # In order to be visible in the picture at some point all the robots will be in different positions
            # and there will be no overlap.
            # If the robots are in different positions, the number of distinct robot positions will be equal to the number of robots
            if len(robot_positions) == robot_count:
                print(sec)
                print_grid(robot_positions, height, width)



def print_grid(final, height, width):
    grid = [[0] * width for _ in range(height)]
    for pos_x, pos_y in final:
        grid[pos_y][pos_x] += 1
    for row in grid:
        print(''.join(map(str, row)).replace('0', '.'))

